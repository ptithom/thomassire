namespace App\Controller;

use Cake\Core\Configure;
use Cake\Network\Exception\ForbiddenException;
use Cake\Network\Exception\NotFoundException;
use Cake\View\Exception\MissingTemplateException;
use Cake\Network\Email\Email;

/**
 * Static content controller
 *
 * This controller will render views from Template/Pages/
 *
 * @link http://book.cakephp.org/3.0/en/controllers/pages-controller.html
 */
class PagesController extends AppController
{

    /**
     * Displays a view
     *
     * @param string ...$path Path segments.
     * @return void|\Cake\Network\Response
     * @throws \Cake\Network\Exception\ForbiddenException When a directory traversal attempt.
     * @throws \Cake\Network\Exception\NotFoundException When the view file could not
     *   be found or \Cake\View\Exception\MissingTemplateException in debug mode.
     */

    public function home(){
        $this->viewBuilder()->setLayout('l_home');

        $this->set('title', 'Thomas sire - développeur web et mobile');

    }

    public function display(...$path)
    {
        $count = count($path);
        if (!$count) {
            return $this->redirect('/');
        }
        if (in_array('..', $path, true) || in_array('.', $path, true)) {
            throw new ForbiddenException();
        }
        $page = $subpage = null;

        if (!empty($path[0])) {
            $page = $path[0];
        }
        if (!empty($path[1])) {
            $subpage = $path[1];
        }
        $this->set(compact('page', 'subpage'));

        try {
            $this->render(implode('/', $path));
        } catch (MissingTemplateException $e) {
            if (Configure::read('debug')) {
                throw $e;
            }
            throw new NotFoundException();
        }
    }

    public function contact(){

        $msg = 'test';

        $data = $this->request->getData();
        if(!empty($data['msg'])){
            $msg = $data['msg'];
            if(!empty($data['name'])){
                $msg = $msg."<br/> ".$data['name']  ;
            }
        }


        $this->set('mail', $msg);

        $email = new Email('default');
        $email->from(['t.sire41@gmail.com' => 'Thomas Sire'])
            ->to('t.sire41@gmail.com')
            ->emailFormat('html')
            ->subject('Contact Portfolio')
            ->send($msg);


        echo json_encode(["error" => 0 , "message" => "Merci, votre email a bien était envoyer."]);

        die();
    }

}
